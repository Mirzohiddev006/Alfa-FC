type LangKey = keyof typeof UZ;
type LangDict = typeof UZ;

const DICTS: Record<string, LangDict> = { uz: UZ, ru: RU };

interface LangCtx {
  lang: string;
  setLang: (l: string) => void;
  t: (key: LangKey) => string;
}

const LangContext = React.createContext<LangCtx>({
  lang: 'uz',
  setLang: () => {},
  t: (k) => UZ[k] ?? k,
});

export function LangProvider({ children }) {
  const [lang, setLangState] = React.useState(() => localStorage.getItem('alpha_lang') || 'uz');

  function setLang(l: string) {
    setLangState(l);
    localStorage.setItem('alpha_lang', l);
  }

  function t(key: LangKey): string {
    return DICTS[lang]?.[key] ?? UZ[key] ?? key;
  }

  return <LangContext.Provider value={{ lang, setLang, t }}>{children}</LangContext.Provider>;
}

export function useT() {
  return React.useContext(LangContext);
}
